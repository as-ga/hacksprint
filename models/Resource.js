import mongoose from "mongoose";

const ResourceSchema = new mongoose.Schema(
  {
    name: {
      type: String,
      required: true,
    },
    description: {
      type: String,
      required: true,
    },
    link: {
      type: String,
      required: true,
    },
  },
  { timestamps: true }
);

export const ResourceModel =
  mongoose.models.Resource || mongoose.model("Resource", ResourceSchema);